// Number
const age = 20;
console.log("age", age, typeof age);

// String
const name = "John";
console.log("name", name, typeof name);

// Simbol
const symbol = Symbol("id");
console.log("symbol", symbol, typeof symbol);

// Null
const nullObject = null;
console.log("nullObject", nullObject, typeof nullObject);

// Undefined
const undefinedObject = undefined;
console.log("undefinedObject", undefinedObject, typeof undefinedObject);

// Boolean
const booleanObject = true;
console.log("booleanObject", booleanObject, typeof booleanObject);

// Object
const objectObject = { name: "John", age: 20 };
console.log("objectObject", objectObject, typeof objectObject);

// BigInteger
const bigIntegerObject = BigInt("1234567890123456789012345678901234567890");
console.log("bigIntegerObject", bigIntegerObject, typeof bigIntegerObject);
